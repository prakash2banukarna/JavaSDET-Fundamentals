public class AddTwoIntegers {
    public int sum(int num1, int num2) {
        return num1+num2;
    }

    public static void main(String[] arg){
        int num1 = 12;
        int num2 = 5;
        AddTwoIntegers obj = new AddTwoIntegers();
        obj.sum(num1,num2);
    }
}
